public class aritmeticExpressions {
    public static void main(String[] args) {
        System.out.println(5+3);
        System.out.println(5-3);
        System.out.println(5*3);
        System.out.println(5/3);
        System.out.println(5%3);
        System.out.println(5.0 / 3.0);

        System.out.println(5L+3);
        System.out.println(5L-3);
        System.out.println(5L*3);
        System.out.println(5L/3);
        System.out.println(5L%3);
        System.out.println(5.0 + 3.0);

    }
}
